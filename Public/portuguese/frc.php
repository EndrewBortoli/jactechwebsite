                <h1>
                    PRINCÍPIOS DA <br> FIRST
                </h1>
                <p>
                A STEAM é um dos mais importantes princípios seguidos pela competição e equipe, sendo eles:
                    <br><br>
                    
                Ciência (Science): É presente princípios da biologia, química, física e ciências da Terra. O componente 
                científico enfatiza a compreensão do mundo natural por meio da observação, experimentação e análise.
<br><br>

                Tecnologia (Technology): Isso abrange o uso de ferramentas, máquinas e sistemas para resolver problemas e 
                melhorar a eficiência, as inovações tecnológicas impulsionam fortemente os demais componentes.
<br><br>

                Engenharia (Engineering): A engenharia envolve o projeto, construção e manutenção de sistemas e estruturas. 
                Isso inclui a aplicação de princípios científicos e matemáticos para criar soluções práticas.

<br><br>
                Artes (Arts): A artes inclui disciplinas como pintura, música, dança, teatro, design gráfico e muito mais, 
                estimulando a criatividade e a inovação.

<br><br>
                Matemática (Mathematics): A matemática é a base, fornecendo as ferramentas quantitativas e lógicas necessárias para 
                a resolução de problemas.

                </p>
