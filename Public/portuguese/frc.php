                </h1>
                <p>
                A FIRST Robotics Competition (FRC) é uma competição internacional promovida pela organização FIRST, onde equipes 
                de estudantes projetam, constroem e programam robôs para cumprir tarefas complexas em um ambiente competitivo. 
                A FRC destaca valores como, preparar os participantes para carreiras em ciência e tecnologia. Ela enfatiza não 
                apenas a construção de robôs, mas também o desenvolvimento de habilidades de liderança, comunicação e resolução 
                de problemas. A competição oferece oportunidades acadêmicas e profissionais.
                </p>
